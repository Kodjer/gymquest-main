// Вспомогательные функции для работы с банком квестов

/**
 * Получить все квесты определённой категории
 */
export function getQuestsByCategory(
  category: "strength" | "cardio" | "flexibility" | "wellness"
): QuestTemplate[] {
  return questBank[category] || [];
}

/**
 * Получить квесты определённого уровня сложности
 */
export function getDifficultyByLevel(
  level: number
): "easy" | "medium" | "hard" {
  if (level <= 5) return "easy";    // Ур. 1-5:  легкие
  if (level <= 11) return "medium"; // Ур. 6-11: средние
  return "hard";                    // Ур. 12+:  сложные
}

/**
 * Рассчитать XP с учётом уровня игрока
 */
export function calculateXP(baseXP: number, playerLevel: number): number {
  // Базовая формула: baseXP * (1 + level * 0.1)
  return Math.floor(baseXP * (1 + playerLevel * 0.1));
}
